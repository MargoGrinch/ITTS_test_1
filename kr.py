"""
Вводиться натуральне число M. Обчислити найбільшу цілу ступінь двійки, яка не перевищує M. Виведіть показник ступеня і
саму ступінь. Операцією зведення в ступінь користуватися не можна. (0 < M ≤ 1000000)
"""
m = float(input('Enter m: '))
c = 1
if 0 < m <= 100000:
    n = 2
else:
    print('Enter correct number:')
while n * 2 < m:
    n *= 2
    c += 1

print(f'Ступінь {n}, показник степення {c} ')
