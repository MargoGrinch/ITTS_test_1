'''
Огороднік М.О. 122А
Вводиться ціле число A. Сформувати і вивести нове число шляхом виписування в зворотному порядку цифр заданого числа.
(0 ≤ A ≤ 1000000)
'''
A = int(input('A = '))
if 0 <= A <= 1000000:
    k = 0
    while A > 0:
        m = A % 10
        A //= 10
        k *= 10
        k += m
    print(f'The number on the contrary is {k}.')
else:
    print('Error. 0 ≤ A ≤ 1000000.')

